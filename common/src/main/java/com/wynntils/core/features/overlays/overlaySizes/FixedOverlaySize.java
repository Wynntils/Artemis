package com.wynntils.core.features.overlays.overlaySizes;

public class FixedOverlaySize extends OverlaySize {
    public FixedOverlaySize(float width, float height) {
        super(width, height);
    }

    @Override
    public float getWidth() {
        return this.width;
    }

    @Override
    public float getHeight() {
        return this.height;
    }
}
